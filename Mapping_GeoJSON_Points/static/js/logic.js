// Accessing the airport GeoJSON URL
let airportData = "https://raw.githubusercontent.com/dvarela1435/Mapping_Earthquakes/main/majorAirports.json";

console.log(airportData)

// Grabbing our GeoJSON data.
d3.json(airportData).then(function(data) {
    console.log(data);
  // Creating a GeoJSON layer with the retrieved data.
  L.geoJSON(data, {
      onEachFeature:function(feature,layer){
          console.log(layer);
          layer.bindPopup("<h2>Airport Code: " + feature.properties.faa + "</h2><h3> Airport Name: " + feature.properties.name + "</h3>");
      }
  }).addTo(map);
});